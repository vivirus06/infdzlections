import math
def площадь_треугольника(s1, s2, s3):
    s = (s1 + s2 + s3) / 2  
    return math.sqrt(s * (s - s1) * (s - s2) * (s - s3))
s1 = float(input("Введите длину стороны s1: "))
s2 = float(input("Введите длину стороны s2: "))
s3 = float(input("Введите длину стороны s3: "))
площадь = площадь_треугольника(s1, s2, s3)
print(f"Площадь треугольника: {площадь:.2f} квадратных единиц")
